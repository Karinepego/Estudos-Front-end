const a = 7 
let b = 3

b += a // b(3) = b(3) + a(7) atribuição aditiva // 3+7=10 (b=10)
console.log(b)

b -= 4 // b(10) = b(10) - 4 atribuição subtrativa // 10-4=6 (b=6)
console.log(b)

b *= 2 // b(6) = b(6) * 2 atribuição multiplicativa // 6*2=12
console.log(b)

b /=2 // b(12) = b(12) / 2 atribuição divisiva // 12/2=6
console.log(b)

b %= 2 // b = b % 2 atribuição modular //6/2=3 resto da divisao = 0
console.log(b)
